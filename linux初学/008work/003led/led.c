
void delay(volatile int d)
{
	while(d--);
}

int main(int which)
{
	unsigned int *pGPFCON = (unsigned int *)0x56000050;
	unsigned int *pGPFDAT = (unsigned int *)0x56000054;

	if(which == 4)
	/* 配置GPF4为输出引脚 */
	*pGPFCON = 0x100;
	else if(which == 6)
	/* 配置GPF6为输出引脚 */
	*pGPFCON = 0x1000;
	
	/* 设置GPF4/6输出0 */
	*pGPFDAT = 0;

	return 0;
}

